#include <stdio.h>


void ft_swap(int *a, int *b)
{
    int x;

    x = *a;
    *a = *b;
    *b = x;
}

int main(void)
{
    int n1;
    int n2;
    n1 = 20;
    n2 = 40;

    printf("%d\n",n1);
    printf("%d\n",n2);

    ft_swap(&n1, &n2);

    printf("%d\n",n1);
    printf("%d\n",n2);

}
